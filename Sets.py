#Sets are used to store multiple items in a single variable. A set is a collection which is unordered, unchangeable*, and unindexed. They are written with {}
thisset = {"apple", "banana", "cherry"}
print(thisset)

#Unorded sets means the items in a set do not have a defined order.
#Sets cannot have two items with the same value.

#You can also use the set() constructor to make a set
thisset2 = set(("apple", "banana", "cherry")) # note the double round-brackets
print(thisset2)



